#data type in python

#integer
x = 10
print(x)
print(type(x))

#float
y = 10.5
print(y)
print(type(y))

#string
z = "Hello"
print(z)

#boolean
a = True
print(a)
print(type(a))

#list
l = [1, 2, 3, 4, 5]
print(l)
print(type(l))

#tuple
t = (1, 2, 3, 4, 5)
print(t)
print(type(t))

#dictionary
d = {"name": "John", "age": 20}
print(d)




